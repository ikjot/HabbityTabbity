import UIKit

extension UIColor {
    
    class func ht_bannerSuccesBackgroundColor() -> UIColor! {
        return UIColor(red:48.00/255.0, green:174.0/255.0, blue:51.5/255.0, alpha:1.000)
    }
    
    class func ht_bannerErrorBackgroundColor() -> UIColor! {
        return UIColor.red
    }
}
